package com.basic;

public class Switchcase {
	public static void main(String[] args) {
		int a=10;
		switch(a) {
		case 5:System.out.println("Given value is valuable");break;
		case 10:System.out.println("Given value is valuable");break;
		case 15:System.out.println("Given value is valuable");break;
		case 20:System.out.println("Given value is valuable");break;
		default:System.out.println("Invalid variable");
		}
		
	}

}
